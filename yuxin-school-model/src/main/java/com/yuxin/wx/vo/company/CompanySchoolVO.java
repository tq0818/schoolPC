package com.yuxin.wx.vo.company;

public class CompanySchoolVO {
	private String item_code;
	private String edu_area_school;
	private String item_value;
	private Integer companyId;
	private String company_name;

	public String getItem_code() {
		return item_code;
	}

	public void setItem_code(String item_code) {
		this.item_code = item_code;
	}

	public String getEdu_area_school() {
		return edu_area_school;
	}

	public void setEdu_area_school(String edu_area_school) {
		this.edu_area_school = edu_area_school;
	}

	public String getItem_value() {
		return item_value;
	}

	public void setItem_value(String item_value) {
		this.item_value = item_value;
	}

	public Integer getCompanyId() {
		return companyId;
	}

	public void setCompanyId(Integer companyId) {
		this.companyId = companyId;
	}

	public String getCompany_name() {
		return company_name;
	}

	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}

}
